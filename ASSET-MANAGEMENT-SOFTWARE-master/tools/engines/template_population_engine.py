    # ══════════════════════════════════════════════════════════════════
    # populate_all — generates all applicable templates per milestone
    # ══════════════════════════════════════════════════════════════════

    @classmethod
    def populate_all(
        cls,
        session_entities: dict[str, list[dict]],
        output_dir: str | Path,
        plant_code: str = "",
        sap_package: dict | None = None,
    ) -> dict[str, Path]:
        """Generate all applicable template xlsx files from session entities.

        Args:
            session_entities: The session.entities dict.
            output_dir: Directory to write xlsx files into.
            plant_code: Plant code for metadata.
            sap_package: Optional SAP upload package dict.

        Returns:
            Dict mapping template filename → output Path.
        """
        out = Path(output_dir)
        out.mkdir(parents=True, exist_ok=True)
        results: dict[str, Path] = {}

        nodes = session_entities.get("hierarchy_nodes", [])
        assessments = session_entities.get("criticality_assessments", [])
        functions = session_entities.get("functions", [])
        functional_failures = session_entities.get("functional_failures", [])
        failure_modes = session_entities.get("failure_modes", [])
        tasks = session_entities.get("maintenance_tasks", [])
        work_packages = session_entities.get("work_packages", [])
        materials = session_entities.get("material_assignments", [])

        # T-01: Equipment Hierarchy
        if nodes:
            try:
                results["01_equipment_hierarchy.xlsx"] = cls.populate_01_hierarchy(
                    nodes, out / "01_equipment_hierarchy.xlsx",
                )
            except Exception:
                logger.warning("Failed to populate T-01 hierarchy", exc_info=True)

        # T-02: Criticality Assessment
        if assessments:
            try:
                results["02_criticality_assessment.xlsx"] = cls.populate_02_criticality(
                    assessments, nodes, out / "02_criticality_assessment.xlsx",
                )
            except Exception:
                logger.warning("Failed to populate T-02 criticality", exc_info=True)

        # T-03: Failure Modes
        if failure_modes:
            try:
                results["03_failure_modes.xlsx"] = cls.populate_03_failure_modes(
                    failure_modes, functions, functional_failures, nodes,
                    out / "03_failure_modes.xlsx",
                )
            except Exception:
                logger.warning("Failed to populate T-03 failure modes", exc_info=True)

        # T-04: Maintenance Tasks
        if tasks:
            try:
                results["04_maintenance_tasks.xlsx"] = cls.populate_04_tasks(
                    tasks, materials, out / "04_maintenance_tasks.xlsx",
                )
            except Exception:
                logger.warning("Failed to populate T-04 tasks", exc_info=True)

        # T-05: Work Packages
        if work_packages:
            try:
                results["05_work_packages.xlsx"] = cls.populate_05_work_packages(
                    work_packages, nodes, out / "05_work_packages.xlsx",
                )
            except Exception:
                logger.warning("Failed to populate T-05 work packages", exc_info=True)

        # T-07: Spare Parts Inventory
        if materials:
            try:
                results["07_spare_parts_inventory.xlsx"] = cls.populate_07_spare_parts(
                    materials, out / "07_spare_parts_inventory.xlsx",
                )
            except Exception:
                logger.warning("Failed to populate T-07 spare parts", exc_info=True)

        # T-14: Maintenance Strategy (cross-cutting — needs FMs + tasks)
        if failure_modes:
            try:
                results["14_maintenance_strategy.xlsx"] = cls.populate_14_strategy(
                    failure_modes, tasks, functions, nodes,
                    out / "14_maintenance_strategy.xlsx",
                )
            except Exception:
                logger.warning("Failed to populate T-14 strategy", exc_info=True)

        logger.info(
            "Template population complete: %d/%d templates generated in %s",
            len(results), 7, out,
        )
        return results
