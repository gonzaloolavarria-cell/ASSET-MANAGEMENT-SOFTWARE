    @staticmethod
    def _save(wb, output_path: Path) -> Path:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        wb.save(output_path)
        logger.info("Template written: %s", output_path)
        return output_path

    @classmethod
    def _safe_get(cls, entity: dict, key: str, default: Any = "") -> Any:
        """Safely get a value from an entity dict."""
        val = entity.get(key, default)
        return val if val is not None else default

    # ══════════════════════════════════════════════════════════════════
    # T-01: Equipment Hierarchy
    # ══════════════════════════════════════════════════════════════════

    # Level strings produced by agents (e.g. "L1_SITE") → int
    _LEVEL_MAP = {
        "L1_SITE": 1, "L1_PLANT": 1,
        "L2_AREA": 2, "L2_PLANT": 2,
        "L3_SYSTEM": 3, "L3_AREA": 3,
        "L4_EQUIPMENT": 4,
        "L5_SUBASSEMBLY": 5, "L5_SUBUNIT": 5,
        "L6_COMPONENT": 6, "L6_MAINTAINABLE_ITEM": 6,
    }

    # ISO 14224 level name mapping
    _ISO_LEVEL_NAMES = {
        1: ("Industry", 5),
        2: ("Business Category", 6),
        3: ("Installation", 7),
        4: ("Plant / Unit", 8),
        5: ("Equipment Class / Unit", 9),
        6: ("Subunit", None),
        7: ("Maintainable Item", None),
    }

    @classmethod
    def _coerce_level(cls, raw) -> int:
        """Coerce level value (int or string like 'L1_SITE') to int."""
        if isinstance(raw, int):
            return raw
        if isinstance(raw, str):
            if raw.upper() in cls._LEVEL_MAP:
                return cls._LEVEL_MAP[raw.upper()]
            for ch in raw:
                if ch.isdigit():
                    return int(ch)
        try:
            return int(raw)
        except (TypeError, ValueError):
            return 0

    @staticmethod
    def _get_meta(node: dict) -> dict:
        """Get metadata dict from node, checking both 'metadata' and 'metadata_json' keys."""
        return node.get("metadata", node.get("metadata_json", {})) or {}

    @classmethod
    def populate_01_hierarchy(
        cls, nodes: list[dict], output_path: Path,
    ) -> Path:
        """Write hierarchy nodes to 01_equipment_hierarchy.xlsx.

        Sheet 1 — Equipment Hierarchy: one row per equipment (L4+), with
        flattened plant/area/system ancestors. Standard 18-column structure.
        Sheet 2 — Equipment BOM: full tree of all nodes per equipment.
        """
        wb = cls._create_workbook("Equipment Hierarchy")

        # ── Shared helpers ────────────────────────────────────────
        by_id: dict[str, dict] = {n.get("node_id", ""): n for n in nodes}

        def _ancestor(node: dict, target_level: int) -> dict:
            current = node
            visited = set()
            while current:
                nid = current.get("node_id", "")
                if nid in visited:
                    break
                visited.add(nid)
                if cls._coerce_level(current.get("level")) == target_level:
                    return current
                parent_id = current.get("parent_node_id")
                current = by_id.get(parent_id, {}) if parent_id else {}
            return {}

        def _node_tag(node: dict) -> str:
            return node.get("tag") or node.get("code") or node.get("node_id", "")

        # ── Sheet 1: Equipment Hierarchy (L4+ only) — standard 18 columns ──
        ws = wb.active
        ws.title = "Equipment Hierarchy"

        headers = [
            "plant_id", "plant_name", "area_code", "area_name",
            "system_code", "system_name", "equipment_tag",
            "equipment_description", "equipment_type", "manufacturer",
            "model", "serial_number", "power_kw", "weight_kg",
            "criticality", "status", "sap_func_loc", "installation_date",
        ]
        cls._style_header(ws, headers)

        equipment_nodes = [
            n for n in nodes if cls._coerce_level(n.get("level", 0)) >= 4
        ]

        for row_num, node in enumerate(equipment_nodes, start=2):
            plant = _ancestor(node, 1)
            area = _ancestor(node, 2)
            system = _ancestor(node, 3)
            meta = cls._get_meta(node)

            cls._write_data_row(ws, [
                _node_tag(plant) if plant else "",
                cls._safe_get(plant, "name"),
                _node_tag(area) if area else "",
                cls._safe_get(area, "name"),
                _node_tag(system) if system else "",
                cls._safe_get(system, "name"),
                _node_tag(node),
                cls._safe_get(node, "name"),
                cls._safe_get(node, "equipment_type",
                              cls._safe_get(meta, "equipment_type",
                                            cls._safe_get(node, "node_type"))),
                cls._safe_get(node, "manufacturer",
                              cls._safe_get(meta, "manufacturer")),
                cls._safe_get(node, "model",
                              cls._safe_get(meta, "model")),
                cls._safe_get(node, "serial_number",
                              cls._safe_get(meta, "serial_number")),
                cls._safe_get(node, "power_kw",
                              cls._safe_get(meta, "power_kw")),
                cls._safe_get(node, "weight_kg",
                              cls._safe_get(meta, "weight_kg")),
                cls._safe_get(node, "criticality"),
                cls._safe_get(node, "status", "ACTIVE"),
                cls._safe_get(node, "sap_func_loc"),
                cls._safe_get(node, "installation_date"),
            ], row_num)

        # Data validations
        cls._add_data_validation(ws, 15, CRITICALITIES)
        cls._add_data_validation(ws, 16, EQUIP_STATUSES)

        cls._auto_size(ws)

        # ── Sheet 2: Equipment BOM (full tree per L4 equipment) ──
        ws_bom = wb.create_sheet("Equipment BOM")
        bom_headers = [
            "equipment_tag", "level", "node_type", "code", "name",
            "name_fr", "parent_code", "order", "mi_category",
            "manufacturer", "iso14224_level", "iso14224_level_name",
        ]
        cls._style_header(ws_bom, bom_headers)

        def _find_equipment_root(node: dict) -> dict:
            current = node
            visited = set()
            while current:
                nid = current.get("node_id", "")
                if nid in visited:
                    break
                visited.add(nid)
                if cls._coerce_level(current.get("level")) == 4:
                    return current
                parent_id = current.get("parent_node_id")
                current = by_id.get(parent_id, {}) if parent_id else {}
            return {}

        sorted_nodes = sorted(
            nodes,
            key=lambda n: (cls._coerce_level(n.get("level", 0)), n.get("node_id", "")),
        )

        order_counter: dict[str, int] = {}
        bom_row = 2
        for node in sorted_nodes:
            level = cls._coerce_level(node.get("level", 0))
            if level < 4:
                continue

            equip_root = _find_equipment_root(node)
            equip_tag = _node_tag(equip_root) if equip_root else _node_tag(node)

            parent_id = node.get("parent_node_id", "")
            parent_node = by_id.get(parent_id, {})
            parent_code = _node_tag(parent_node) if parent_node and cls._coerce_level(
                parent_node.get("level", 0)) >= 4 else None

            parent_key = parent_id or "__root__"
            order_counter.setdefault(parent_key, 0)
            order_counter[parent_key] += 1

            iso_level = level + 1
            iso_name = cls._ISO_LEVEL_NAMES.get(iso_level, ("", None))[0]

            bom_meta = cls._get_meta(node)
            cls._write_data_row(ws_bom, [
                equip_tag,
                level,
                cls._safe_get(node, "node_type",
                              cls._safe_get(node, "equipment_type")),
                _node_tag(node),
                cls._safe_get(node, "name"),
                cls._safe_get(node, "name_fr"),
                parent_code,
                order_counter[parent_key] - 1,
                cls._safe_get(node, "mi_category"),
                cls._safe_get(node, "manufacturer",
                              cls._safe_get(bom_meta, "manufacturer")),
                iso_level,
                iso_name,
            ], bom_row)
            bom_row += 1

        cls._auto_size(ws_bom)

        # Instructions
        cls._add_instructions_sheet(wb, INSTR_01_HIERARCHY)

        return cls._save(wb, output_path)

    # ══════════════════════════════════════════════════════════════════
    # T-02: Criticality Assessment
    # ══════════════════════════════════════════════════════════════════

    CRIT_CATEGORIES = [
        "safety", "health", "environment", "production", "operating_cost",
        "capital_cost", "schedule", "revenue", "communications",
        "compliance", "reputation",
    ]

    @classmethod
    def populate_02_criticality(
        cls,
        assessments: list[dict],
        nodes: list[dict],
        output_path: Path,
    ) -> Path:
        """Write criticality assessments to 02_criticality_assessment.xlsx."""
        wb = cls._create_workbook("Criticality Assessment")
        ws = wb.active
        ws.title = "Criticality Assessment"

        headers = ["equipment_tag", "method"] + cls.CRIT_CATEGORIES + ["probability"]
        cls._style_header(ws, headers)

        by_id = {n.get("node_id", ""): n for n in nodes}

        for row_num, ca in enumerate(assessments, start=2):
            node = by_id.get(ca.get("node_id", ""), {})
            tag = node.get("tag", node.get("code", ca.get("node_id", "")))
            method = cls._safe_get(ca, "method", "FULL_MATRIX")

            score_map: dict[str, int | str] = {}
            for cs in ca.get("criteria_scores", []):
                cat_key = str(cs.get("category", "")).lower()
                val = cs.get("score", cs.get("consequence_level", ""))
                score_map[cat_key] = val

            consequences = ca.get("consequences", ca)
            cat_scores = []
            for cat in cls.CRIT_CATEGORIES:
                val = score_map.get(cat, "")
                if val == "":
                    val = cls._safe_get(consequences, cat, "")
                cat_scores.append(val)

            cls._write_data_row(ws, [
                tag, method,
                *cat_scores,
                cls._safe_get(ca, "probability", cls._safe_get(ca, "likelihood", "")),
            ], row_num)

        # Data validations
        cls._add_data_validation(ws, 2, CRIT_METHODS)
        for col_idx in range(3, 14):
            cls._add_data_validation(ws, col_idx, ["1", "2", "3", "4", "5"])

        cls._auto_size(ws)
        cls._add_instructions_sheet(wb, INSTR_02_CRITICALITY)
        return cls._save(wb, output_path)

