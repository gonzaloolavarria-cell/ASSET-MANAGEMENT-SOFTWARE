def _generate_intent_profile(
    submission: "RFISubmission",
    client_slug: str,
    project_slug: str,
) -> None:
    """Generate a minimal intent-profile.yaml from RFI responses (IP-L1 only).

    IP-L2 and IP-L3 are left empty — they require a client interview.
    """
    summary: dict[str, Any] = {
        "client": getattr(submission.company_site, "company_name", client_slug),
        "project": getattr(submission.company_site, "plant_name", project_slug),
        "trade_off_priority": ["safety", "availability", "cost"],
        "risk_appetite": "moderate",
        "primary_kpi": "availability",
        "primary_kpi_target": 90,
        "hard_limits": [],
    }

    # Extract from KPI section if available
    kpi = getattr(submission, "kpi_baseline", None)
    if kpi:
        if getattr(kpi, "primary_kpi", None):
            summary["primary_kpi"] = kpi.primary_kpi
        if getattr(kpi, "primary_target", None):
            summary["primary_kpi_target"] = kpi.primary_target

    profile = {
        "schema_version": "1.0",
        "intent_summary": summary,
        "domain_intent": {
            "reliability": {},
            "planning": {},
            "spare_parts": {},
        },
        "full_context": {},
        "trade_off_matrix": [],
        "veto_rules": [],
    }

    intent_dir = get_intent_specs_dir(client_slug, project_slug)
    intent_dir.mkdir(parents=True, exist_ok=True)
    intent_file = intent_dir / "intent-profile.yaml"
    with open(intent_file, "w", encoding="utf-8") as f:
        yaml.dump(profile, f, default_flow_style=False, allow_unicode=True)
    logger.info("Generated intent profile: %s", intent_file)


def _append_or_create(path: Path, content: str, dry_run: bool = False) -> None:
    """Append content to file if it exists, create if not. Never overwrites.

    Idempotent: does not duplicate content if already present.
    """
    if dry_run:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.exists():
        existing = path.read_text(encoding="utf-8")
        if content.strip() not in existing:
            with open(path, "a", encoding="utf-8") as f:
                f.write("\n\n" + content)
            logger.info("Appended to: %s", path)
    else:
        path.write_text(content, encoding="utf-8")
        logger.info("Created: %s", path)


def generate_followup(
    submission: RFISubmission,
    validation_results: list,
) -> str | None:
    """Generate rfi-followup.md for missing required data.

    Returns None if no followup is needed (all required fields filled).
    """
    errors = [r for r in validation_results if r.severity == "ERROR"]
    warnings = [r for r in validation_results if r.severity == "WARNING"]

    if not errors and not warnings:
        return None

    lines = [
        "# RFI Follow-Up Items",
        "",
        f"Generated: {date.today().isoformat()}",
        f"Client: {submission.company_site.company_name}",
        "",
    ]

    if errors:
        lines.extend([
            "## Required Items (MUST resolve before proceeding)",
            "",
        ])
        for r in errors:
            lines.append(f"- **[{r.rule_id}]** {r.message}")
        lines.append("")

    if warnings:
        lines.extend([
            "## Recommended Items (improve quality)",
            "",
        ])
        for r in warnings:
            lines.append(f"- [{r.rule_id}] {r.message}")

    return "\n".join(lines)


