def main() -> None:
    parser = argparse.ArgumentParser(
        description="Process completed AMS RFI questionnaire",
    )
    parser.add_argument(
        "input_excel",
        type=Path,
        help="Path to the completed RFI Excel file (.xlsx)",
    )
    parser.add_argument(
        "--client", "-c",
        required=True,
        help="Client slug (e.g., 'ocp')",
    )
    parser.add_argument(
        "--project", "-p",
        required=True,
        help="Project slug (e.g., 'jfc-maintenance-strategy')",
    )
    parser.add_argument(
        "--scaffold",
        action="store_true",
        help="Create project directory structure if it doesn't exist",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Validate and generate content without writing files",
    )
    args = parser.parse_args()

    logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")

    result = process_rfi(
        args.input_excel,
        args.client,
        args.project,
        scaffold=args.scaffold,
        dry_run=args.dry_run,
    )

    # Print summary
    errors = sum(1 for v in result["validation"] if v["severity"] == "ERROR")
    warnings = sum(1 for v in result["validation"] if v["severity"] == "WARNING")

    print(f"\nRFI Processing Complete")
    print(f"  Completeness: {result['completeness_score']:.0f}%")
    print(f"  Validation: {errors} errors, {warnings} warnings")
    print(f"  Files generated: {len(result['files'])}")

    if not args.dry_run:
        for path_str in result["files"]:
            print(f"    -> {path_str}")

    if errors > 0:
        print(f"\n  WARNING: {errors} validation errors found. Review rfi-followup.md")


if __name__ == "__main__":
    main()
