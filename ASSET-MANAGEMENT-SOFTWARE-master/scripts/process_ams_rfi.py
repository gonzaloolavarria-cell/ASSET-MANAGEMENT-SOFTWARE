# ---------------------------------------------------------------------------
# Safe cell readers (pattern from OR SYSTEM)
# ---------------------------------------------------------------------------

def _safe_str(value: Any) -> str:
    """Convert cell value to stripped string, empty string for None."""
    if value is None:
        return ""
    return str(value).strip()


def _safe_int(value: Any, default: int | None = None) -> int | None:
    """Convert cell value to int, returning default for invalid/empty."""
    if value is None:
        return default
    try:
        return int(float(value))
    except (ValueError, TypeError):
        return default


def _safe_float(value: Any, default: float | None = None) -> float | None:
    """Convert cell value to float, returning default for invalid/empty."""
    if value is None:
        return default
    try:
        return float(value)
    except (ValueError, TypeError):
        return default


def _safe_bool(value: Any) -> bool | None:
    """Convert cell value to bool. Recognizes Yes/No/True/False/Partial."""
    if value is None:
        return None
    s = str(value).strip().lower()
    if s in ("yes", "true", "1", "partial"):
        return True
    if s in ("no", "false", "0"):
        return False
    return None


def _split_list(value: Any, sep: str = ";") -> list[str]:
    """Split cell value into list, stripping whitespace."""
    if value is None:
        return []
    return [item.strip() for item in str(value).split(sep) if item.strip()]


def _safe_date(value: Any) -> date | None:
    """Convert cell value to date."""
    if value is None:
        return None
    if isinstance(value, datetime):
        return value.date()
    if isinstance(value, date):
        return value
    try:
        return datetime.strptime(str(value).strip(), "%Y-%m-%d").date()
    except (ValueError, TypeError):
        return None


def _safe_enum(value: Any, enum_cls: type) -> Any:
    """Convert cell value to enum member, returning None if invalid."""
    s = _safe_str(value)
    if not s:
        return None
    # Try direct match
    for member in enum_cls:
        if member.value == s:
            return member
    # Try case-insensitive
    s_lower = s.lower()
    for member in enum_cls:
        if member.value.lower() == s_lower:
            return member
    return None


# ---------------------------------------------------------------------------
# Response column index (column C = index 3 in openpyxl)
# ---------------------------------------------------------------------------

_RESPONSE_COL = 3  # Column C


def _get_responses(ws) -> dict[str, Any]:
    """Extract field_id -> response value mapping from a questionnaire sheet.

    Reads rows starting from row 2. Column A = field ID, Column C = response.
    """
    responses: dict[str, Any] = {}
    for row in ws.iter_rows(min_row=2, max_col=6, values_only=False):
        field_id = _safe_str(row[0].value)  # Column A
        if not field_id:
            continue
        responses[field_id] = row[_RESPONSE_COL - 1].value  # Column C
    return responses


