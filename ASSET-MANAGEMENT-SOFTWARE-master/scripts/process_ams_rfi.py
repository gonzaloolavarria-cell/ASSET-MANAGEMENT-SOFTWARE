def parse_data_availability(ws) -> DataAvailabilityChecklist:
    """Parse Sheet 8: Data Availability Checklist."""
    items: list[DataAvailabilityItem] = []
    for row in ws.iter_rows(min_row=2, max_col=6, values_only=False):
        template_id = _safe_str(row[0].value)
        if not template_id:
            continue
        template_name = _safe_str(row[1].value)
        items.append(DataAvailabilityItem(
            template_id=template_id,
            template_name=template_name,
            available=_safe_bool(row[2].value),
            data_format=_safe_enum(row[3].value, DataFormat),
            quality_score=_safe_int(row[4].value),
            notes=_safe_str(row[5].value),
        ))
    return DataAvailabilityChecklist(items=items)


