def _slugify(text: str) -> str:
    """Convert text to a URL-friendly slug."""
    text = text.lower().strip()
    text = re.sub(r"[^\w\s-]", "", text)
    text = re.sub(r"[\s_]+", "-", text)
    text = re.sub(r"-+", "-", text)
    return text.strip("-")


def _val_or_none(v: Any) -> Any:
    """Return value or None if empty string."""
    if isinstance(v, str) and v == "":
        return None
    return v


def build_project_yaml(submission: RFISubmission) -> dict:
    """Build project.yaml dict from RFI data. Matches OCP reference format."""
    s = submission
    cs = s.company_site
    ms = s.maintenance_state
    kpi = s.kpi_baseline
    st = s.scope_timeline
    org = s.organization
    std = s.standards

    def _enum_val(e: Any) -> str | None:
        return e.value if e is not None else None

    # Determine availability target as fraction (0-1)
    availability_target = None
    if kpi.target_availability is not None:
        availability_target = round(kpi.target_availability / 100, 2)

    # Check template availability
    def _template_available(tid: str) -> bool:
        for item in s.data_availability.items:
            if item.template_id == tid and item.available is True:
                return True
        return False

    return {
        "project": {
            "id": f"{_slugify(cs.country or 'xx')}-{_slugify(cs.plant_name or 'unknown')}-001",
            "name": f"{cs.plant_name} Maintenance Strategy Development" if cs.plant_name else "Maintenance Strategy Development",
            "version": "1.0",
            "created_at": date.today().isoformat(),
        },
        "client": {
            "slug": _slugify(cs.company_name) if cs.company_name else "",
            "name": _val_or_none(cs.company_name),
            "industry": _enum_val(cs.industry),
            "country": _val_or_none(cs.country),
            "language": _enum_val(cs.primary_language),
        },
        "scope": {
            "type": _enum_val(st.scope_type),
            "plant": {
                "name": _val_or_none(cs.plant_name),
                "code": _val_or_none(cs.plant_code),
                "location": _val_or_none(cs.location),
            },
            "priority_equipment": st.priority_equipment or [],
            "timeline": {
                "start_date": st.start_date.isoformat() if st.start_date else None,
                "target_completion": st.target_completion.isoformat() if st.target_completion else None,
            },
            "areas_in_scope": st.areas_in_scope or [],
        },
        "maintenance_context": {
            "strategy_maturity": _enum_val(ms.strategy_maturity),
            "cmms": _enum_val(ms.cmms_type),
            "sap_version": _enum_val(ms.sap_version),
            "criticality_method": _enum_val(ms.criticality_method),
            "availability_target": availability_target,
            "existing_data": {
                "equipment_list": s.equipment_hierarchy.equipment_list_available or False,
                "failure_history": ms.failure_data_available or False,
                "existing_pm_plans": ms.planned_maintenance_exists or False,
                "spare_parts_inventory": _template_available("07_spare_parts_inventory"),
                "bom_data": s.equipment_hierarchy.bom_available or False,
            },
        },
        "organization": {
            "team_size": org.team_size,
            "structure": _enum_val(org.org_structure),
            "shifts": org.shifts,
            "trades": org.trades or [],
        },
        "standards": {
            "iso_certifications": std.iso_certifications or [],
            "safety_requirements": std.safety_permits_required or [],
            "procedure_language": _enum_val(std.procedure_language),
        },
        "kpi_baseline": {
            "availability": kpi.current_availability,
            "mtbf_hours": kpi.current_mtbf_hours,
            "mttr_hours": kpi.current_mttr_hours,
            "oee": kpi.current_oee,
            "planned_vs_unplanned": kpi.current_planned_vs_unplanned,
            "pm_compliance": kpi.current_pm_compliance,
        },
    }


def generate_data_availability_report(submission: RFISubmission) -> str:
    """Generate data-availability-report.md markdown content."""
    lines = [
        "# Data Availability Report",
        "",
        f"Generated: {date.today().isoformat()}",
        f"Client: {submission.company_site.company_name}",
        f"Plant: {submission.company_site.plant_name}",
        "",
        "## Data Availability Matrix",
        "",
        "| Template | Available | Format | Quality | Notes |",
        "|----------|-----------|--------|---------|-------|",
    ]

    available_count = 0
    for item in submission.data_availability.items:
        avail = "Yes" if item.available else ("No" if item.available is False else "?")
        if item.available:
            available_count += 1
        fmt = item.data_format.value if item.data_format else "-"
        quality = str(item.quality_score) if item.quality_score else "-"
        notes = item.notes or "-"
        lines.append(f"| {item.template_name} | {avail} | {fmt} | {quality} | {notes} |")

    total = len(submission.data_availability.items) or 1
    pct = (available_count / total) * 100

    lines.extend([
        "",
        "## Summary",
        "",
        f"- **Data available**: {available_count}/{total} templates ({pct:.0f}%)",
        f"- **Data gaps**: {total - available_count} templates without data",
    ])

    return "\n".join(lines)


def generate_scope_assessment(submission: RFISubmission) -> str:
    """Generate scope-assessment.md with effort estimation."""
    cs = submission.company_site
    st = submission.scope_timeline
    eh = submission.equipment_hierarchy
    ms = submission.maintenance_state

    equip_count = eh.estimated_equipment_count or 0
    scope = st.scope_type.value if st.scope_type else "unknown"

    # Simple effort heuristics
    if equip_count <= 50:
        complexity = "Low"
    elif equip_count <= 200:
        complexity = "Medium"
    elif equip_count <= 500:
        complexity = "High"
    else:
        complexity = "Very High"

    has_history = ms.wo_history_available or False
    has_criticality = ms.prior_criticality_assessment or False
    has_fmeca = ms.prior_fmeca or False

    available_data = sum(
        1 for item in submission.data_availability.items if item.available
    )

    lines = [
        "# Scope Assessment",
        "",
        f"Generated: {date.today().isoformat()}",
        f"Client: {cs.company_name}",
        f"Plant: {cs.plant_name}",
        "",
        "## Scope Summary",
        "",
        f"- **Scope type**: {scope}",
        f"- **Equipment count**: {equip_count}",
        f"- **Complexity**: {complexity}",
        f"- **Areas**: {', '.join(st.areas_in_scope) if st.areas_in_scope else 'Not specified'}",
        f"- **Priority equipment**: {', '.join(st.priority_equipment) if st.priority_equipment else 'Not specified'}",
        "",
        "## Starting Point Assessment",
        "",
        f"- Existing WO history: {'Yes' if has_history else 'No'}",
        f"- Prior criticality assessment: {'Yes' if has_criticality else 'No'}",
        f"- Prior FMECA: {'Yes' if has_fmeca else 'No'}",
        f"- Data templates available: {available_data}/14",
        "",
        "## Timeline",
        "",
        f"- Start: {st.start_date.isoformat() if st.start_date else 'TBD'}",
        f"- Target completion: {st.target_completion.isoformat() if st.target_completion else 'TBD'}",
        "",
        "## Recommended Starting Milestone",
        "",
    ]

    if has_criticality and has_fmeca:
        lines.append("Start at **Milestone 3** (Strategy + Tasks) — prior criticality and FMECA exist.")
    elif has_criticality:
        lines.append("Start at **Milestone 2** (FMEA) — prior criticality exists, skip hierarchy.")
    else:
        lines.append("Start at **Milestone 1** (Hierarchy + Criticality) — full analysis required.")

    return "\n".join(lines)


def generate_global_requirements(submission: RFISubmission) -> str:
    """Generate global-requirements.md seeded with RFI data."""
    cs = submission.company_site
    std = submission.standards

    lang = cs.primary_language.value if cs.primary_language else "en"
    proc_lang = std.procedure_language.value if std.procedure_language else lang

    lines = [
        "# Global Requirements",
        "",
        f"Generated from RFI: {date.today().isoformat()}",
        "",
        "## Language",
        "",
        f"- Primary language: {lang}",
        f"- Procedure language: {proc_lang}",
        f"- Work instructions MUST be written in: **{proc_lang}**",
    ]

    if cs.secondary_language:
        lines.append(f"- Secondary language: {cs.secondary_language.value}")

    lines.extend(["", "## Naming Conventions", ""])

    eh = submission.equipment_hierarchy
    if eh.naming_convention:
        lines.append(f"- Convention: {eh.naming_convention}")
    if eh.tag_format_example:
        lines.append(f"- Tag example: {eh.tag_format_example}")
    if eh.functional_location_structure:
        lines.append(f"- Functional location: {eh.functional_location_structure}")
    if not eh.naming_convention and not eh.tag_format_example:
        lines.append("- No naming convention specified — use AMS defaults")

    lines.extend(["", "## Standards", ""])

    if std.iso_certifications:
        lines.append(f"- ISO certifications: {', '.join(std.iso_certifications)}")
    if std.safety_permits_required:
        lines.append(f"- Safety permits: {', '.join(std.safety_permits_required)}")
    if std.lototo_program:
        lines.append("- LOTOTO program: Active")
    if std.industry_regulations:
        lines.append(f"- Industry regulations: {', '.join(std.industry_regulations)}")

    lines.extend(["", "## Criticality Method", ""])

    ms = submission.maintenance_state
    if ms.criticality_method:
        lines.append(f"- Method: {ms.criticality_method.value}")
        lines.append("- Override allowed: No")
    else:
        lines.append("- Method: Not specified — use R8 + GFSN default")

    lines.extend(["", "## CMMS Integration", ""])

    if ms.cmms_type:
        lines.append(f"- CMMS: {ms.cmms_type.value}")
    if ms.sap_version:
        lines.append(f"- SAP version: {ms.sap_version.value}")
    if ms.cmms_type == CMMSType.SAP_PM:
        lines.append("- SAP field length constraints apply to all outputs")

    lines.extend(["", "## Hard Constraints", ""])
    lines.append("<!-- Non-negotiable constraints that override ALL methodology defaults. -->")

    st = submission.scope_timeline
    if hasattr(st, "constraints") and st.constraints:
        for constraint in st.constraints:
            lines.append(f"- {constraint}")
    else:
        lines.append("- None specified")

    return "\n".join(lines)


