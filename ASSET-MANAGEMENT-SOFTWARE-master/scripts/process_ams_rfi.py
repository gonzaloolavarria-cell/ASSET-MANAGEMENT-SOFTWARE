def process_rfi(
    excel_path: Path,
    client_slug: str,
    project_slug: str,
    scaffold: bool = False,
    dry_run: bool = False,
) -> dict[str, Any]:
    """Process a filled RFI Excel into project configuration files.

    Args:
        excel_path: Path to the completed RFI Excel file.
        client_slug: Client identifier (e.g., 'ocp').
        project_slug: Project identifier (e.g., 'jfc-maintenance-strategy').
        scaffold: If True, create project directory structure first.
        dry_run: If True, validate and generate content but don't write files.

    Returns:
        Dict with 'files' (dict of path->content), 'validation' (list),
        'completeness_score' (float).
    """
    excel_path = Path(excel_path).resolve()
    if not excel_path.exists():
        raise FileNotFoundError(f"RFI Excel not found: {excel_path}")
    if not excel_path.suffix.lower() == ".xlsx":
        raise ValueError(f"Expected .xlsx file, got: {excel_path.suffix}")

    # Scaffold project if requested
    if scaffold and not dry_run:
        scaffold_project(client_slug, project_slug)

    # Load workbook (data_only=True to read calculated values)
    wb = load_workbook(str(excel_path), data_only=True)

    # Parse all sheets (by title prefix matching)
    submission = RFISubmission(
        company_site=parse_company_site(_find_sheet(wb, "1-Company")),
        equipment_hierarchy=parse_equipment_hierarchy(_find_sheet(wb, "2-Equipment")),
        maintenance_state=parse_maintenance_state(_find_sheet(wb, "3-Maintenance")),
        organization=parse_organization(_find_sheet(wb, "4-Organization")),
        standards=parse_standards(_find_sheet(wb, "5-Standards")),
        kpi_baseline=parse_kpi_baseline(_find_sheet(wb, "6-KPI")),
        scope_timeline=parse_scope_timeline(_find_sheet(wb, "7-Scope")),
        data_availability=parse_data_availability(_find_sheet(wb, "8-Data")),
    )

    # Validate
    validation_results = RFIValidator.run_full_validation(submission)
    score, _ = RFIValidator.calculate_completeness(submission)

    # Generate outputs
    project_yaml_dict = build_project_yaml(submission)
    project_yaml_str = yaml.dump(
        project_yaml_dict,
        default_flow_style=False,
        allow_unicode=True,
        sort_keys=False,
    )
    availability_report = generate_data_availability_report(submission)
    scope_assessment = generate_scope_assessment(submission)
    global_requirements = generate_global_requirements(submission)
    followup = generate_followup(submission, validation_results)

    # Generate memory-seeding content from remaining RFI sheets
    maint_strategy_req = generate_maintenance_strategy_requirements(submission)
    work_planning_req = generate_work_planning_requirements(submission)
    reliability_req = generate_reliability_requirements(submission)
    cost_analysis_req = generate_cost_analysis_requirements(submission)
    standards_appendix = generate_standards_appendix(submission)
    kpi_appendix = generate_kpi_appendix(submission)

    # Resolve output paths
    project_root = get_project_root(client_slug, project_slug)
    output_dir = get_output_dir(client_slug, project_slug)
    memory_dir = get_memory_dir(client_slug, project_slug)

    rfi_output_dir = output_dir / "rfi"

    files: dict[str, str] = {
        str(project_root / "project.yaml"): project_yaml_str,
        str(rfi_output_dir / "data-availability-report.md"): availability_report,
        str(rfi_output_dir / "scope-assessment.md"): scope_assessment,
        str(memory_dir / "global-requirements.md"): global_requirements,
    }
    if followup:
        files[str(rfi_output_dir / "rfi-followup.md")] = followup

    # Write files (unless dry run)
    if not dry_run:
        for path_str, content in files.items():
            p = Path(path_str)
            p.parent.mkdir(parents=True, exist_ok=True)
            p.write_text(content, encoding="utf-8")
            logger.info("Wrote: %s", p)

    # Phase 9: Seed additional memory files (append-only, idempotent)
    _append_or_create(
        memory_dir / "maintenance-strategy" / "requirements.md",
        maint_strategy_req,
        dry_run=dry_run,
    )
    _append_or_create(
        memory_dir / "work-planning" / "requirements.md",
        work_planning_req,
        dry_run=dry_run,
    )
    _append_or_create(
        memory_dir / "global-requirements.md",
        standards_appendix,
        dry_run=dry_run,
    )
    _append_or_create(
        memory_dir / "global-requirements.md",
        kpi_appendix,
        dry_run=dry_run,
    )
    _append_or_create(
        memory_dir / "reliability-engineering" / "requirements.md",
        reliability_req,
        dry_run=dry_run,
    )
    _append_or_create(
        memory_dir / "cost-analysis" / "requirements.md",
        cost_analysis_req,
        dry_run=dry_run,
    )

    # Generate intent profile from RFI data (IP-L1 only)
    if not dry_run:
        _generate_intent_profile(submission, client_slug, project_slug)

    # Add memory-seeded files to result tracking
    memory_files = {
        str(memory_dir / "maintenance-strategy" / "requirements.md"): maint_strategy_req,
        str(memory_dir / "work-planning" / "requirements.md"): work_planning_req,
        str(memory_dir / "reliability-engineering" / "requirements.md"): reliability_req,
        str(memory_dir / "cost-analysis" / "requirements.md"): cost_analysis_req,
    }
    files.update(memory_files)

    return {
        "files": files,
        "validation": [
            {"rule_id": r.rule_id, "severity": r.severity, "message": r.message}
            for r in validation_results
        ],
        "completeness_score": score,
    }


def _find_sheet(wb, prefix: str):
    """Find worksheet by title prefix (case-insensitive)."""
    prefix_lower = prefix.lower()
    for ws in wb.worksheets:
        if ws.title.lower().startswith(prefix_lower):
            return ws
    raise ValueError(f"Sheet with prefix '{prefix}' not found in workbook")


